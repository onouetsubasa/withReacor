  class var mainTextBlack: UIColor { return #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1) }
  
  // メイン文字色(#666666)
  class var mainTextGray: UIColor { return #colorLiteral(red: 0.4, green: 0.4, blue: 0.4, alpha: 1) }
  
